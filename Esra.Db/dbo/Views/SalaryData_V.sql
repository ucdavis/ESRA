CREATE VIEW dbo.SalaryData_V
AS
SELECT     TITLE_CODE AS TitleCode, TITLE_NAME AS PayrollTitle, EFF_BEGIN_DATE AS EffectiveDate, TITLE_UNIT_CODE AS BargainingCode, 
                      PAY_REP AS RepresentationCode, PAY_INTERVAL AS SalaryStep, NUMBER_OF_PAY_INTERVALS AS NumSalarySteps, RATE_LOOKUP_FLAG AS RateLookupFlag, 
                      (CASE WHEN [SALARY_GRADE_FLAG] IS NULL THEN 'N' ELSE [SALARY_GRADE_FLAG] END) AS HasSalaryGrades, SALARY_GRADE AS SalaryGrade, 
                      PAY_RATE_ANNUAL AS Annual, PAY_RATE_MONTHLY AS Monthly, PAY_RATE_HOURLY_RND AS HourlyRnd, SALARY_RANGE_MINIMUM AS MinAnnual, 
                      SALARY_RANGE_10TH_PCTLE, SALARY_RANGE_25TH_PCTLE, SALARY_RANGE_MIDPOINT AS MidAnnual, SALARY_RANGE_75TH_PCTLE, 
                      SALARY_RANGE_90TH_PCTLE, SALARY_RANGE_MAXIMUM AS MaxAnnual
FROM         dbo.SalaryData_20120510
